package com.cys.utils

class Datos {

    //Byte
    fun byteKilobyte(byte: Double):Double{
        println("Paso de byte a kilobyte")
        return byte / 1000
    }

    fun byteMegabyte(byte: Double):Double{
        println("Paso de byte a megabyte")
        return byte / 1000000
    }

    fun byteGigabyte(byte: Double):Double{
        println("Paso de byte a gigabyte")
        return byte / 1000000000
    }

    fun byteTerabyte(byte: Double):Double{
        println("Paso de byte a terabyte")
        return byte / 1000000000000
    }


